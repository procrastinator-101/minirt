#include "../../includes/minirt.h"

t_rgb	uint_to_rgb(unsigned color)
{
	t_rgb	rgb;

	rgb.red = ((color << 8) >> 24) / 255.0;
	rgb.green = ((color << 16) >> 24) / 255.0;
	rgb.blue = (color & 255) / 255.0;
	return (rgb);
}
